package Basics;

public class typeCasting {
    public static void main(String[] args) {
        byte i=9;
        short j=i;
        System.out.println(j);
        short k=120;
        byte l=(byte)k;
        System.out.println(l);
        double m=3.2;
        int n=(int)m;
        System.out.println(n);
        int o=3;
        float p=o;
        System.out.println(p);
    }
}
