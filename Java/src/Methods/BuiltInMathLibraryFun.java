package Methods;

public class BuiltInMathLibraryFun {
    public static void main(String[] args) {
        System.out.println(Math.pow(3.14,2));
        System.out.println(Math.sqrt(8));
        System.out.println(Math.cbrt(8));
        System.out.println(Math.abs(-5));
        System.out.println(Math.ceil(4.2));
        System.out.println(Math.floor(4.2));
        System.out.println(Math.min(4,3));
        System.out.println(Math.random());
        System.out.println(Math.max(4,Math.max(5,6)));
    }
}
