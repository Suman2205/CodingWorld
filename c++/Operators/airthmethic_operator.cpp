#include<iostream>
using namespace std;
int main(){
    int a;
    cout<<"Enter first number:"<<endl;
    cin>>a;
    int b;
    cout<<"Enter second number:"<<endl;
    cin>>b;
    cout<<"Sum:"<<a+b<<endl;
    cout<<"Subtract:"<<a-b<<endl;
    cout<<"multiplication:"<<a*b<<endl;
    cout<<"division:"<<a/b<<endl;
    cout<<"Remainder:"<<a%b<<endl;
    cout<<"Increment:"<<a++<<endl;
    cout<<"Decrement:"<<a--<<endl;
    return 0;
}